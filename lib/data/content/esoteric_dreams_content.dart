/// Ezoterik Rüya İçerik Servisi
class EzoterikRuyaIcerikServisi {
  /// Bilinç halleri
  static List<BilincHali> bilincHalleri() => TantrikBilincHalleri.tumHaller();

  /// Yoga nidra aşamaları
  static List<YogaNidraAsamasi> yogaNidraAsamalari() =>
      YogaNidraRehberi.asamalar;

  /// Enerji kılıfları
  static List<EnerjiKilifi> enerjiKiliflari() => AstralBedenVeRuya.besKilif;

  /// Astral seyahat türleri
  static List<AstralSeyahatTuru> astralSeyahatTurleri() =>
      AstralBedenVeRuya.seyahatTurleri;

  /// Çakra rüya bilgileri
  static List<CakraRuyaBilgisi> cakraRuyalari() => CakraRuyalari.tumCakralar();

  /// Gezegen rüya bilgileri
  static List<GezegenRuyaBilgisi> gezegenRuyalari() =>
      GezegenRuyaYorumlari.tumGezegenler();

  /// Burç rüya bilgileri
  static List<BurcRuyaBilgisi> burcRuyalari() => BurcRuyaTemalari.tumBurclar();

  /// Tantrik semboller
  static List<TantrikSembolBilgisi> tantrikSemboller() =>
      TantrikRuyaSembolleri.tumSemboller();

  /// Rüya pratikleri
  static List<RuyaPratigi> ruyaPratikleri() => RuyaPratikleri.tumPratikler();

  /// Tüm mantralar
  static List<RuyaMantrasi> tumMantralar() =>
      RuyaMantraVeDualari.tumMantralar();

  /// İsme göre çakra bul
  static CakraRuyaBilgisi? cakraBul(String isim) {
    try {
      return cakraRuyalari().firstWhere(
        (c) =>
            c.cakraAdi.toLowerCase() == isim.toLowerCase() ||
            c.turkceAdi.toLowerCase().contains(isim.toLowerCase()),
      );
    } catch (_) {
      return null;
    }
  }

  /// İsme göre gezegen bul
  static GezegenRuyaBilgisi? gezegenBul(String isim) {
    try {
      return gezegenRuyalari().firstWhere(
        (g) => g.gezegen.toLowerCase() == isim.toLowerCase(),
      );
    } catch (_) {
      return null;
    }
  }

  /// İsme göre burç bul
  static BurcRuyaBilgisi? burcBul(String isim) {
    try {
      return burcRuyalari().firstWhere(
        (b) => b.burc.toLowerCase() == isim.toLowerCase(),
      );
    } catch (_) {
      return null;
    }
  }

  /// Rüya sembolünü analiz et (basit eşleştirme)
  static Map<String, dynamic> sembolAnalizi(String sembol) {
    final sonuc = <String, dynamic>{
      'sembol': sembol,
      'cakra': <CakraRuyaBilgisi>[],
      'gezegen': <GezegenRuyaBilgisi>[],
      'burc': <BurcRuyaBilgisi>[],
      'tantrik': <TantrikSembolBilgisi>[],
    };

    final sembolLower = sembol.toLowerCase();

    // Çakra eşleştirmesi
    for (final cakra in cakraRuyalari()) {
      if (cakra.pozitifRuyalar
              .any((r) => r.toLowerCase().contains(sembolLower)) ||
          cakra.dengesizRuyalar
              .any((r) => r.toLowerCase().contains(sembolLower))) {
        (sonuc['cakra'] as List).add(cakra);
      }
    }

    // Gezegen eşleştirmesi
    for (final gezegen in gezegenRuyalari()) {
      if (gezegen.semboller
              .any((s) => s.toLowerCase().contains(sembolLower)) ||
          gezegen.pozitifRuyalar
              .any((r) => r.toLowerCase().contains(sembolLower)) ||
          gezegen.olumsuzRuyalar
              .any((r) => r.toLowerCase().contains(sembolLower))) {
        (sonuc['gezegen'] as List).add(gezegen);
      }
    }

    // Burç eşleştirmesi
    for (final burc in burcRuyalari()) {
      if (burc.tipikRuyaSembolleri
          .any((s) => s.toLowerCase().contains(sembolLower))) {
        (sonuc['burc'] as List).add(burc);
      }
    }

    // Tantrik eşleştirme
    for (final tantrik in tantrikSemboller()) {
      if (tantrik.ruyaTurleri
          .any((r) => r.toLowerCase().contains(sembolLower))) {
        (sonuc['tantrik'] as List).add(tantrik);
      }
    }

    return sonuc;
  }
}
